package com.sanklan;

public class AsciiValues {
    public static void main(String[] args){
        int smallA = 'a';   // ASCII for a is 97
        int capitalA = 'A';     // ASCII for A is 65
        System.out.println(smallA);
        System.out.println(capitalA);
        System.out.println("नमस्ते"); // it will print namaste in hindi as Java follows unicode scheme
    }
}
